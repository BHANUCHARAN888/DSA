# Time Complexity: It tells how does run-time grow as input size grows?
# --> It measures in Big - O notation
# --> Constant time O(1)
# --> Linear time(single loop) O(n)
# --> Quadratic time(multi loop) O(n^2)
# --> Logarithmic time O(log n) --- Each step cuts search space in half
# --> O(n log n) used for merge and heap sort, better than O(n^2)

# Space Complexity: how much extra memory is used?
# --> O(1) fixed memory
# --> O(n) extra array grows with input size

# Complexity n -> 2n
# O(1)       same
# O(log n)   slightly big
# O(n)       2x
# O(n^2)     4x
# O(n^3)     8x